package ch01.lecture;

public class C03Print {
    public static void main(String[] args){
        // sout
        System.out.println("hi");

        // \n : 새로운 줄
        System.out.println("hello\njava");

        // \t : 탭
        System.out.println("hello\tjava");

        // \\ : \
        System.out.println("hello\\java");

        // \" : "
        System.out.println("hello\"java");

    }
}
