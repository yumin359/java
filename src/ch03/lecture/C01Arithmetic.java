package ch03.lecture;

public class C01Arithmetic {
    public static void main(String[] args) {
        // 산술연산자
        // +, -, *, /, %

        // +
        int a = 3;
        int b = 5;
        System.out.println("(a+b) = " + (a + b));
        // -
        System.out.println("(a - b) = " + (a - b));
        // *
        System.out.println("(a * b) = " + (a * b));
        // /
        System.out.println("(a / b) = " + (a / b));

        // % (modulo 연산, remainder 연산)
        System.out.println("(a % b) = " + (a % b));
    }
}
