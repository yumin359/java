package ch04.lecture;

public class C02Else {
    public static void main(String[] args) {
        
        // else : if의 조건이 false일 때 실행되는 코드 블럭

        if (true) {
            System.out.println("코드!!!");
        } else {
            System.out.println("코드@@@");
        }

        if (false) {
            System.out.println("코드###");
        } else {
            System.out.println("코드$$$");
        }
    }
}
